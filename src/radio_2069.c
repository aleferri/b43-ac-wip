/*
 * Per-core AFE + radio-LPF stage setup, run at the tail of the channel setup.
 *
 * Reconstructed from wl-diag-d6220 down->bss-up #51609-51677 and #51893-51965.
 * Both occurrences share this exact per-core body and differ only in the
 * 0x0728 field 0x3800 (0x0800 vs 0x0000, here 'afe_728') and in a caller-side
 * preamble (0x08ea write / 0x02d1-2 mods) that is NOT part of this helper.
 * The radio 0x0049 writes read back 0x0030 / 0x0000 in the trace: those are the
 * RMW results of clearing already-set-or-clear fields over differing prior
 * state, not literals -- hence plain field masks here. Iterates num_cores (all
 * present cores are powered at channel-setup time, like the reset-time blocks),
 * not the antenna coremask. Exact purpose unverified; named for its registers.
 * Call site (verified by fingerprint segmentation): op_software_rfkill, right
 * after b43_radio_2069_rccal -- #51609-51677, immediately following the
 * 0x08ea RCCAL_EN cleanup (#51606) and before the op_init set_pdet (#51679).
 * afe_728 = 0x0800 in the ch36/5GHz capture. A second call with afe_728=0x0000
 * is also observed in the same bring-up; its exact position is not yet pinned,
 * so only the 0x0800 call is wired here.
 */
void b43_radio_2069_afe_lpf_stage(struct b43_wldev *dev, u16 afe_728)
{
	static const u16 clr49[] = {
		0xe000, 0x1800, 0x0400, 0x0300, 0x0040, 0x0080,
	};
	u8 core, num_cores = dev->phy.ac->num_cores;
	unsigned int k;

	for (core = 0; core < num_cores; core++) {
		u16 co = core * 0x200;

		b43_phy_maskset(dev, 0x0728 + co, (u16)~0x3800, afe_728);
		b43_phy_maskset(dev, 0x0721 + co, (u16)~0x4000, 0x4000);
	}
	for (core = 0; core < num_cores; core++) {
		u16 co = core * 0x200;

		b43_radio_set(dev, 0x0045 + co, 0x0080);
		for (k = 0; k < ARRAY_SIZE(clr49); k++)
			b43_radio_mask(dev, 0x0049 + co, (u16)~clr49[k]);
	}
}

/* 4360 agcombo: 4112-4177 ; d6220 ch36 @#32954 (fp 10/16) */
void b43_radio_2069_channel_setup(struct b43_wldev *dev,
	const struct b43_phy_ac_channeltab_e_radio2069 *e)
{
	const struct r2069_chan_write *writes = r2069_chan_writes;
	unsigned int n = ARRAY_SIZE(r2069_chan_writes);
	unsigned int i;

	if (!e)
		return;

	/* AFE-gate pulse 0x0728 bit 8 (0x0100). Presente in tutte le catture
	 * d6220 (attach/attach-bw40/attach-ch44/dtu) e agcombo (attach + dtu)
	 * subito dopo il freeze RX e prima delle RAD.WR di setup canale.
	 * Semantica non confermata da RE; il bit 8 di 0x0728 è toccato anche
	 * da b43_radio_2069_afe_lpf_stage (set+clear), quindi è un trigger
	 * AFE-related. d6220 ch36 #32902-32903, agcombo ch36 #4102-4103. */
	b43_phy_maskset(dev, 0x0728, (u16)~0x0100, 0x0100);
	b43_phy_maskset(dev, 0x0728, (u16)~0x0100, 0x0000);

	if (b43_current_band(dev->wl) == NL80211_BAND_2GHZ) {
		writes = r2069_chan_writes_2g;
		n = ARRAY_SIZE(r2069_chan_writes_2g);
	}

	for (i = 0; i < n; i++)
		b43_radio_write(dev, writes[i].reg,
				r2069_pick_value(e, writes[i].raw_idx));

	/* Radio cal constants, right after channel table writes. ch36 #32949-32952. */
	b43_radio_write(dev, 0x066d, 0x18c4);
	b43_radio_write(dev, 0x0893, 0x0001);
	b43_radio_write(dev, 0x0145, 0x0185);
	b43_radio_write(dev, 0x0146, 0x00ac);

	/* 0x0723 = 0x03e0: canale-invariante in tutte le catture d6220+agcombo,
	 * subito prima del maskset 0x0645. ch36 #32953. */
	b43_radio_write(dev, 0x0723, 0x03e0);

	b43_radio_maskset(dev, 0x0645, (u16)~0x7000, 0x7000);

	// Theory: only for 5Ghz
	if (b43_current_band(dev->wl) == NL80211_BAND_5GHZ) {
		b43_radio_maskset(dev, 0x08c9, (u16)~0xff00, 0x0000);
		b43_radio_maskset(dev, 0x08c9, (u16)~0x00ff, 0x0002);
		b43_radio_write(dev,   0x08ca, 0x0002);
		b43_radio_maskset(dev, 0x08cc, (u16)~0x00ff, 0x0002);
		b43_radio_maskset(dev, 0x08cc, (u16)~0xff00, 0xff00);
		b43_radio_write(dev,   0x08c7, 0xffff);
		b43_radio_write(dev,   0x08c8, 0xffff);
	}

	b43_radio_write(dev,   0x0723, 0x83e0);

	/*
	 * VCO/PLL enable. Le rfpll writes sopra fissano la reference, questa
	 * sequenza fa il kick del loop; il PLL locca negli ~15us degli udelay
	 * sotto. Il lock verify (RAD.RD 0x090b bit 8) è spostato in adc_reset:
	 * il vendor emette il peek subito prima del release RX finale (ch36
	 * #38205), non dentro chanspec_set. Semantica: il chip resta nel
	 * freeze RX per decine di ms mentre il flow di channel_setup gira,
	 * quindi il lock è già ampiamente assicurato quando serve verificarlo.
	 */
	b43_radio_maskset(dev, 0x08e5, (u16)~0x4000, 0x0000);
	b43_radio_maskset(dev, 0x08d0, (u16)~0x0001, 0x0000);
	b43_radio_maskset(dev, 0x08e8, (u16)~0x0040, 0x0000);
	b43_radio_maskset(dev, 0x08dc, (u16)~0x2000, 0x0000);
	udelay(11);
	b43_radio_maskset(dev, 0x08d0, (u16)~0x0001, 0x0001);
	b43_radio_maskset(dev, 0x08e8, (u16)~0x0040, 0x0040);
	udelay(1);
	b43_radio_maskset(dev, 0x08dc, (u16)~0x2000, 0x2000);
	udelay(60);
}



