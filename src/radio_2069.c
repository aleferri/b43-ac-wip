/* AFE calibration (per-core); shares the cal-block enable R2069_RCCAL_EN1. */
#define R2069_AFE_CAL_CLK	0x121	/* [12] per-core cal clock enable */
#define R2069_AFE_CAL_CTRL	0x122	/* [3:0] cal-enable bits */
#define R2069_AFE_CAL_STAT	0x144	/* [1]=done (1st read) [0]=valid (2nd) */
#define R2069_AFECAL_CFG	0x5830	/* fixed CTRL config; enable nibble = 0x000f (dtbu trace #56978: WR 0x583f then 0x5830) */

static inline void r2069_mod(struct b43_wldev *dev, u16 reg, u16 mask, u16 val)
{
	b43_radio_maskset(dev, reg, (u16)~mask, val & mask);
}

static const u16 b43_r2069_rccal_seed[3] = { 0x014a, 0x0101, 0x011a };
static const u8  b43_r2069_rccal_init[3] = { 1, 0, 0 };
static const u8  b43_r2069_rccal_mode[3] = { 0, 2, 1 };
static const u8  b43_r2069_rccal_nloop[3] = { 0x1c, 0x70, 0x40 };

/* Pass 0/1 common preamble: program the engine for this pass. */
static void b43_r2069_rccal_setup(struct b43_wldev *dev, unsigned int pass)
{
	r2069_mod(dev, R2069_RCCAL_CFG, 0x1000,
		  (u16)(b43_r2069_rccal_init[pass] << 12));
	r2069_mod(dev, R2069_RCCAL_CFG, 0x0018,
		  (u16)(b43_r2069_rccal_mode[pass] << 3));
	r2069_mod(dev, R2069_RCCAL_KICK, 0xff00,
		  (u16)(b43_r2069_rccal_nloop[pass] << 8));
	b43_radio_write(dev, R2069_RCCAL_SEED,
			b43_r2069_rccal_seed[pass]);
}

/* Kick the engine and poll the done bit. */
static bool b43_r2069_rccal_run(struct b43_wldev *dev)
{
	unsigned int i;

	r2069_mod(dev, R2069_RCCAL_CFG, 0x0001, 0x0000);
	udelay(1);
	r2069_mod(dev, R2069_RCCAL_CFG, 0x0001, 0x0001);
	udelay(35);
	r2069_mod(dev, R2069_RCCAL_KICK, 0x0001, 0x0001);

	for (i = 0; i < R2069_RCCAL_POLL_MAX; i++) {
		udelay(100);
		if (b43_radio_read(dev, R2069_RCCAL_STAT) & 0x0010)
			break;
	}

	r2069_mod(dev, R2069_RCCAL_KICK, 0x0001, 0x0000);

	return i < R2069_RCCAL_POLL_MAX;
}

/* Pass 1: write the measured 5-bit RC code into the per-core trim regs. */
static void b43_r2069_rccal_apply_code(struct b43_wldev *dev)
{
	u16 code = b43_radio_read_log(dev, R2069_RCCAL_G) & 0x001f;
	unsigned int core;

	/*
	 * Per silicon core: RC-time-constant trim applies to every powered core,
	 * incl. the antenna-less core 2 (all three cores).
	 * Unlike AFE cal (afecal), this is not per-active-chain.
	 */
	for (core = 0; core < dev->phy.ac->num_cores; core++) {
		u16 stride = (u16)(core << 9);

		r2069_mod(dev, (R2069_RCCAL_P1_A | stride) & 0xff3e, 0x001f, code);
		r2069_mod(dev, (R2069_RCCAL_P1_B | stride) & 0xfe4b, 0x001f, code);
	}
}

/*
 * Pass 2 arm: set the fixed per-core trim before running the engine.
 * P2_A clears bit 2 (RC-fast trim off), P2_B sets bit 13 (dacbuf-cap
 * measurement enable). The engine runs with these armed; P2_B is then
 * disarmed by b43_r2069_rccal_disarm_p2b after reading the result.
 */
static void b43_r2069_rccal_apply_fixed(struct b43_wldev *dev)
{
	unsigned int core;

	/* Per silicon core, as apply_code (all three). */
	for (core = 0; core < dev->phy.ac->num_cores; core++) {
		u16 stride = (u16)(core << 9);

		r2069_mod(dev, (R2069_RCCAL_P2_A | stride) & 0xff3f, 0x0004, 0x0000);
		r2069_mod(dev, (R2069_RCCAL_P2_B | stride) & 0xfff5, 0x2000, 0x2000);
	}
}

/* Pass 2 disarm: clear the P2_B measurement-enable bit after the engine
 * has run and dacbuf_cap has been read. d6220 ch36 #32718-32726. */
static void b43_r2069_rccal_disarm_p2b(struct b43_wldev *dev)
{
	unsigned int core;

	for (core = 0; core < dev->phy.ac->num_cores; core++) {
		u16 stride = (u16)(core << 9);

		r2069_mod(dev, (R2069_RCCAL_P2_B | stride) & 0xfff5,
			  0x2000, 0x0000);
	}
}

/* 4360 agcombo: 174-258 ; d6220 ch36 #32586-32732 (EN1 enable -> 3 pass -> EN1 disable) */
void b43_radio_2069_rccal(struct b43_wldev *dev)
{
	unsigned int pass;

	u16 chipnum = dev->dev->chip_id;

	if (chipnum != 0x4352 && chipnum != 0x4360) {
		b43dbg(dev->wl, "radio 2069: rccal skipped, chipnum 0x%04x not supported\n",
		       chipnum);
		return;
	}

	r2069_mod(dev, R2069_RCCAL_EN1, 0x0080, 0x0080);
	r2069_mod(dev, R2069_RCCAL_EN2, 0x0600, 0x0400);

	/*
	 * First two passes
	 */
	for (pass = 0; pass < 2; pass++) {
		b43_r2069_rccal_setup(dev, pass);
		if (b43_r2069_rccal_run(dev)) {
			if (pass == 0) {
				s16 e = b43_radio_read_log(dev, R2069_RCCAL_E);
				s16 f = b43_radio_read_log(dev, R2069_RCCAL_F);
				u8 cap = (u8)(((f - e) * 193) >> 8);

				dev->phy.ac->lpf_cap0 = cap;
				dev->phy.ac->lpf_cap1 = cap;
				b43dbg(dev->wl,
				       "radio 2069: rccal lpf cap 0x%02x (E=0x%04x F=0x%04x)\n",
				       cap, (u16)e, (u16)f);
			} else {
				b43_r2069_rccal_apply_code(dev);
			}
		}
	}

	/*
	 * Last pass (dacbuf cap): the engine runs with P2_A/P2_B armed, 
	 * then the measurement-enable bit in P2_B is cleared. d6220 ch36 #32671-32729.
	 */
	b43_r2069_rccal_setup(dev, pass);
	b43_r2069_rccal_apply_fixed(dev);
	b43_r2069_rccal_run(dev);
	dev->phy.ac->dacbuf_cap = (b43_radio_read(dev, R2069_RCCAL_G) & 0x03e0) >> 5;
	b43_r2069_rccal_disarm_p2b(dev);

	r2069_mod(dev, R2069_RCCAL_CFG, 0x0001, 0x0000);

	r2069_mod(dev, R2069_RCCAL_EN1, 0x0080, 0x0000);
}

/*
 * Per-core analog-front-end calibration for the 2069 radio.
 *
 * Two passes over the active chains, faithful to the d6220 down-to-bss-up
 * capture (#56954-57000): arm every core (route the PHY in, gate the cal
 * clock off), settle, then launch every core (gate the clock on, set the
 * AFE_CAL_CTRL enable nibble over the fixed 0x5830 config, poll done/valid,
 * drop the nibble). Completion is two STAT reads per try -- done from the
 * first, valid from the second. The post-cal PHY writes are fixed constants,
 * not a restore of the arm-time values.
 */
/* 4360 agcombo: 8784-8829 ; down-to-bss-up #56954-57000 ; d6220 ch36: n/l */
void b43_radio_2069_afecal(struct b43_wldev *dev)
{
	struct b43_phy_ac *ac = dev->phy.ac;
	unsigned int core;

	/* Enable the shared analog cal block (same gate rccal uses). */
	r2069_mod(dev, R2069_RCCAL_EN1, 0x0080, 0x0080);

	/* Pass 1: arm every active core with the cal clock gated off. */
	for (core = 0; core < ac->num_cores; core++) {
		u16 rbase = (u16)(core << 9);	/* per-core radio window */
		u16 pbase = (u16)(core << 9);	/* per-core PHY window */

		if (!((ac->coremask >> core) & 1))
			continue;

		/*
		 * Il vendor a #38084-#38086 (core 0), #38121-#38123 (core 1) emette
		 * 3 peek diagnostici sui registri PHY prima dei maskset arm.
		 * Serve a "toccare" i registri per lo stato pre-arm (non usato
		 * nei calcoli, solo campionato dal tracer).
		 */
		b43_phy_read_log(dev, 0x0739 + pbase);
		b43_phy_read_log(dev, 0x073a + pbase);
		b43_phy_read_log(dev, 0x0725 + pbase);

		b43_phy_maskset(dev, 0x0739 + pbase, (u16)~0x0080, 0x0080);
		b43_phy_maskset(dev, 0x0725 + pbase, (u16)~0x0004, 0x0004);
		r2069_mod(dev, R2069_AFE_CAL_CLK | rbase, 0x1000, 0x0000);
	}

	udelay(1);

	/* Pass 2: launch every active core, then finalize its PHY regs. */
	for (core = 0; core < ac->num_cores; core++) {
		u16 rbase = (u16)(core << 9);
		u16 pbase = (u16)(core << 9);
		u16 post[2] = { 0, 0 }, ctrl = 0;

		if (!((ac->coremask >> core) & 1))
			continue;

		/*
		 * Clock un-gate is the AFE_CAL_CLK[12] bit toggled by the two
		 * r2069_mod above (off in pass 1, on here); trace #56962/70
		 * (off) and #56974/86 (on) confirm it, so no extra gating is
		 * needed. The blob then does a bare readback of AFE_CAL_CTRL
		 * (#56977 core0, #56989 core1) before writing the enable word:
		 * the value isn't used for the write (which is absolute,
		 * CFG|0x000f, not a RMW), but we route it through _log so the
		 * real pre-enable CTRL state is captured on hardware -- the
		 * trace only has the address, the value was the hole we're
		 * filling. It's surfaced in the per-core b43dbg below.
		 */
		r2069_mod(dev, R2069_AFE_CAL_CLK | rbase, 0x1000, 0x1000);
		ctrl = b43_radio_read_log(dev, R2069_AFE_CAL_CTRL | rbase);

		/*
		 * Il vendor legge STAT solo DOPO l'arm write (#38105-#38106),
		 * NON prima. Cioè: readback CTRL (pre-arm) + WR CTRL arm + 2×
		 * RD STAT (post-arm). Poi WR CTRL disarm.
		 */
		b43_radio_write(dev, R2069_AFE_CAL_CTRL | rbase,
				R2069_AFECAL_CFG | 0x000f);
		post[0] = b43_radio_read_log(dev, R2069_AFE_CAL_STAT | rbase);
		udelay(5);
		post[1] = b43_radio_read_log(dev, R2069_AFE_CAL_STAT | rbase);
		udelay(10);

		b43dbg(dev->wl,
		       "radio 2069: afecal core %u ctrl=0x%04x stat post(0/1/10us)=0x%04x/0x%04x\n",
		       core, ctrl, post[0], post[1]);

		b43_radio_write(dev, R2069_AFE_CAL_CTRL | rbase, R2069_AFECAL_CFG);

		b43_phy_write(dev, 0x0739 + pbase, 0x0000);
		b43_phy_write(dev, 0x073a + pbase, 0x0180);
		b43_phy_write(dev, 0x0725 + pbase, 0x0600);
	}

	/* Disable the cal block. */
	r2069_mod(dev, R2069_RCCAL_EN1, 0x0080, 0x0000);
}

/*
 * Band-preferred radio register block for radio_rev 4, run from
 * b43_radio_2069_init right after the power-on setup pulse, before the
 * chip-id-gated body. Moved here from phy_ac.c: this table and its writer
 * only ever touch radio registers (b43_radio_write), same as the rest of
 * this file -- it was added to phy_ac.c by mistake in the patch that
 * introduced it.
 */
static const u16 b43_r2069_prefregs_rev4[][2] = {
	{ 0x063a, 0x0000 }, { 0x063d, 0x000f }, { 0x0645, 0x3000 },
	{ 0x0646, 0x0303 }, { 0x0647, 0x36e2 }, { 0x0649, 0x6000 },
	{ 0x064a, 0x0003 }, { 0x065e, 0x0ff4 }, { 0x0666, 0x0b2d },
	{ 0x0667, 0x03ff }, { 0x0115, 0x3337 },
	{ 0x0117, 0x1337 }, { 0x0718, 0x7777 }, { 0x071c, 0x1000 },
	{ 0x0726, 0x0100 }, { 0x0727, 0x0002 }, { 0x0761, 0x0100 },
	{ 0x0407, 0x8382 }, { 0x055e, 0x0020 }, { 0x0885, 0x0700 },
	{ 0x08e9, 0x01ce }, { 0x08ea, 0x00ff }, { 0x08ec, 0x06ff },
	{ 0x0972, 0x0600 },
};

static void b43_r2069_prefregs_init(struct b43_wldev *dev)
{
	unsigned int i;

	for (i = 0; i < ARRAY_SIZE(b43_r2069_prefregs_rev4); i++)
		b43_radio_write(dev, b43_r2069_prefregs_rev4[i][0],
				b43_r2069_prefregs_rev4[i][1]);
}

/*
 * Radio 2069 power-on init. Called once from op_init. Moved here from
 * phy_ac.c (see comment on b43_r2069_prefregs_init above): the only PHY
 * register touches in here are the save/restore of 0x0728 and the fixed
 * gating writes around the radio body, the same pattern already used by
 * b43_radio_2069_afecal in this file.
 */
/* 4360 agcombo: 86-97 ; d6220 ch36 @#32421 (fp 10/24) */
void b43_radio_2069_init(struct b43_wldev *dev)
{
	u16 saved_728;

	saved_728 = b43_phy_read_log(dev, 0x0728);
	b43_phy_read_log(dev, 0x0408);	/* #51263: base letta prima delle write sotto; loggata per HW (write assoluta) */

	/* --- Prologo: PHY register writes --- */
	b43_phy_write(dev, 0x0415, 0x0000);
	b43_phy_write(dev, 0x040e, 0x0000);
	b43_phy_write(dev, 0x040c, 0x2000);
	b43_phy_write(dev, B43_PHY_AC_RFCTL_CMD, 0x0c00);
	b43_phy_write(dev, 0x0417, 0x0000);
	b43_phy_write(dev, 0x0416, 0x000d);

	/* 0x728 &= 0x7e7f (clears 0x8000/0x0100/0x0080); wl7 writes 0x720 = 0x03ff. */
	b43_phy_write(dev, 0x0728, saved_728 & 0x7e7f);
	b43_phy_read_log(dev, 0x0720);	/* #51271: base loggata per HW; write sotto assoluta */
	b43_phy_write(dev, 0x0720, 0x03ff);
	b43_phy_write(dev, B43_PHY_AC_RFCTL_CMD, 0x0c00);

	/* --- Setup + udelay(100) --- */
	udelay(100);
	b43_phy_write(dev, B43_PHY_AC_RFCTL_CMD, 0x0c01);
	b43_phy_write(dev, B43_PHY_AC_RFCTL_CMD, 0x0c00);

	if (dev->phy.radio_rev == 4)
		b43_r2069_prefregs_init(dev);

	/* --- Body (acphychipid ∈ {0x4360, 0x4352}) --- */
	b43_radio_set(dev, 0x096b, 0x0800);
	b43_radio_set(dev, 0x096b, 0x4000);
	b43_radio_set(dev, 0x096c, 0x0800);
	b43_radio_set(dev, 0x096b, 0x8000);
	b43_radio_set(dev, 0x096b, 0x1000);
	b43_radio_set(dev, 0x096b, 0x0004);
	b43_radio_set(dev, 0x0407, 0x0002);
	b43_radio_set(dev, 0x055e, 0x0010);	/* -> 0x0030 (0x0020|0x0010) */

	{
		unsigned int core;

		/*
		 * All num_cores silicon cores are still powered here: this runs
		 * at rfkill-time, before channel_setup->rxcore_setstate, which
		 * only masks the active RX chains (it does not power a core down).
		 * The wl blob touches every core's radio space at this point
		 * (incl. core 2 on the 2x2 d6220), so gate
		 * on presence (num_cores), not on the antenna/active-chain mask.
		 */
		for (core = 0; core < dev->phy.ac->num_cores; core++) {
			u16 stride = (u16)(core * 0x200);

			b43_radio_maskset(dev, 0x0126 + stride, (u16)~0x0300, 0x0100);
			b43_radio_maskset(dev, 0x0127 + stride, (u16)~0x0003, 0x0002);
			b43_radio_maskset(dev, 0x006f + stride, (u16)~0x0004, 0x0000);
			b43_radio_maskset(dev, 0x006f + stride, (u16)~0x0001, 0x0000);
			b43_radio_maskset(dev, 0x006f + stride, (u16)~0x0002, 0x0000);
			b43_radio_maskset(dev, 0x0065 + stride, (u16)~0x0001, 0x0000);
		}
	}

	/* --- Epilogue (chip-agnostic), restores the SAVED 0x728 --- */
	/* radio 0x040c bit4 low, PHY 0x0408 pulse, bit4 high. */
	b43_radio_mask(dev, 0x040c, (u16)~0x0010);
	b43_phy_write(dev, B43_PHY_AC_RFCTL_CMD, 0x0c06);
	b43_radio_set(dev, 0x040c, 0x0010);
	b43_phy_write(dev, 0x0417, 0x000d);
	b43_phy_write(dev, B43_PHY_AC_RFCTL_CMD, 0x0c02);
	b43_phy_write(dev, 0x0728, saved_728 | 0x0180);
	udelay(100);
	b43_phy_write(dev, 0x0417, 0x0004);
	b43_phy_write(dev, 0x0728, saved_728 & 0xfeff);
}

/*
 * Radio power-on sequence, run once right after b43_radio_2069_init
 * during software-rfkill unblock (op_software_rfkill). Moved here from
 * phy_ac.c: it was inlined between b43_radio_2069_init and
 * b43_radio_2069_rccal -- pure radio-register block, no PHY touches,
 * same misplacement as the prefregs/init functions above.
 */
/* 4360 agcombo: 148-172 ; d6220 ch36 @#32547 (fp 10/17) */
void b43_radio_2069_pwron(struct b43_wldev *dev)
{
	u16 pon0, pon1;

	b43_radio_set(dev,   0x08ea, 0x0040);
	b43_radio_set(dev,   0x08ea, 0x0080);
	b43_radio_mask(dev,  0x08ed, ~0x0600);
	b43_radio_mask(dev,  0x08ed, ~0x1800);
	b43_radio_set(dev,   0x0548, 0x0001);
	b43_radio_write(dev, 0x0549, 0x0000);
	b43_radio_write(dev, 0x054a, 0x0000);
	b43_radio_write(dev, 0x054b, 0x0000);
	b43_radio_write(dev, 0x054c, 0x0000);
	b43_radio_mask(dev,  0x040b, ~0x0001);
	udelay(1);
	b43_radio_set(dev,   0x040b, 0x0001);	/* power-on kick */
	udelay(10);

	/*
	 * Power-on readback (#51420-51421): the blob reads 0x040b twice right
	 * after the kick+udelay. The register is HW-updated during the wait --
	 * the later mask writes back 0x0168 (#51433), i.e. 0x040b had risen to
	 * 0x0169 on its own. Whether the pair is a done/valid poll or a
	 * settle/flush is not decidable from the trace (values UNDEFINED); log
	 * it so a live boot can tell us, then act on it.
	 */
	pon0 = b43_radio_read(dev, 0x040b);
	pon1 = b43_radio_read(dev, 0x040b);

	b43_radio_mask(dev,  0x0548, ~0x0001);
	b43_radio_mask(dev,  0x08ea, ~0x0040);
	b43_radio_mask(dev,  0x08ea, ~0x0080);
	b43_radio_mask(dev,  0x040b, ~0x0001);

	/* pwron tail (#51434-51439), dropped by the read-less attach port */
	b43_radio_set(dev,     0x08ea, 0x0080);
	b43_radio_maskset(dev, 0x08ed, (u16)~0x0600, 0x0400);

	b43dbg(dev->wl, "phy-ac: radio pwron 0x040b readback: %04x %04x\n",
	       pon0, pon1);
}
